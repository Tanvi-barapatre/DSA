// What is a Nested Loop?
// - A nested loop means using one loop inside another loop.
// - The inner loop executes completely for each iteration of the outer loop.

//  **Outer loop → controls the number of passes.

//  **Inner loop → executes repeatedly for every outer loop pass.

//SYNTAX

// for(initialization; condition; update) {      // Outer loop
//     for(initialization; condition; update) {  // Inner loop
//         // Code to be executed
//     }
// }

