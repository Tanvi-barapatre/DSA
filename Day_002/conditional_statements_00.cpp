//Types of Conditional Statements :

// 1. if	            : Executes a block if a condition is true
// 2. if-else	        : Executes one block if true, another if false
// 3. else-if ladder	: Tests multiple conditions in sequence
// 4. nested if	        : if inside another if
// 5. switch	        : Tests one variable against many constant values
// 6. Ternary (? :)	    : Shortcut for simple if-else


//Loops : They are control structures used to execute a block of code repeatedly, as long as a certain condition is true.

//Types of Loops :

// 1. for loop          : A loop used when the number of iterations is known beforehand. It includes initialization, condition-checking, and updating in a single line.
// 2. while loop        : A loop that executes as long as a given condition is true. It is used when the number of iterations is not known in advance.
// 3. do-while loop     : A loop similar to the while loop, but it guarantees at least one execution of the loop body before checking the condition.


