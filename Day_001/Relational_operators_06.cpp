#include <iostream>
using namespace std;

int main() {

//Relational operators
    
    int a, b;
    cout << "Enter the value of 'a' : ";
    cin >> a;
    cout << "Enter the value of 'b' : ";
    cin >> b;

    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    cout << (a == b) << endl;
    cout << (a != b) << endl;
    return 0;
}
